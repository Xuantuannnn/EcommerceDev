// trung gian kết nối giữa nodejs và mongodb, giúp cho cơ sở dữ liệu chặt chẽ hơn, có tính ràng buộc hơn
const mongoose = require("mongoose");

// Tạo model
const productSchema = mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  richDescription: {
    type: String,
    default: "",
  },
  image: {
    type: String,
    default: "",
  },
  images: [
    {
      type: String,
    },
  ],
  brand: {
    type: String,
    default: "",
  },
  price: {
    type: Number,
    default: 0,
  },
  category: {
    type: mongoose.Schema.Types.ObjectId,
    ref: "Category",
    required: true,
  },
  countInStock: {
    type: Number,
    required: true,
    min: 0,
    max: 255,
  },
  rating: {
    type: Number,
    default: 0,
  },
  numReviews: {
    type: Number,
    default: 0,
  },
  isFeatured: {
    type: Boolean,
    default: false,
  },
  dateCreated: {
    type: Date,
    default: Date.now,
  },
});
// cái chuỗi Product đầu ở model() nó sẽ tự chuyển hóa sang products trong mongodb
exports.Product = mongoose.model("Product", productSchema); // như này thì phải trong object
